    /**
     * Number of times to repeat tests
     */
    private int repeat = 5;

    /**
     * Sizes of the system matrix
     */
    protected int max = 50, bmax = 10;

    /**
     * Numerical tolerance
     */
    protected float rtol = 1e-3f;

    protected float rmse = 1e-2f;

    /**
     * Diagonal shift for singularity handling
     */
    protected float shift = 100;

    /**
     * Square system matrix
     */
    protected FloatMatrix2D A;

    /**
     * Right hand side, right hand for transpose system, and the solution vector
     * in both cases
     */
    protected FloatMatrix1D b, bt, x;

    /**
     * Stores the data of x
     */
    protected FloatMatrix1D xd;

    /**
     * Iterative solver to use
     */
    protected FloatIterativeSolver solver;

    /**
     * Preconditioner to use
     */
    protected FloatPreconditioner M;

    /**
     * Constructor for IterativeSolverTest
     */
    public FloatIterativeSolverTest(String arg0) {
        super(arg0);
    }

    protected void setUp() throws Exception {
        createMatrix();

        int n = A.rows();
        x = new DenseFloatMatrix1D(n);
        b = new DenseFloatMatrix1D(n);
        bt = new DenseFloatMatrix1D(n);

        // Create solver and preconditioner
        createSolver();
        FloatIterationMonitor im = new DefaultFloatIterationMonitor(1000000, rtol, 1e-25f, 1e+3f);
        //        im.setIterationReporter(new NoFloatIterationReporter() {
        //
        //            
        //            public void monitor(float r, FloatMatrix1D x, int i) {
        //                System.out.println("Norm of residual = " + r);
        //            }
        //
        //            
        //            public void monitor(float r, int i) {
        //                System.out.println("Residual = " + r);
        //            }
        //
        //        });
        if (!(solver instanceof FloatHyBR)) {
            solver.setIterationMonitor(im);
        }
        M.setMatrix(A);
        solver.setPreconditioner(M);
        x.assign(1);
        // Compute the correct right hand sides
        b = A.zMult(x, b);
        bt = A.zMult(x, bt, 1, 0, true);

        // Store x for later. It is overwritten
        xd = x.copy();

        x = new DenseFloatMatrix1D(n);
    }

    protected abstract void createSolver() throws Exception;

    protected void createMatrix() throws Exception {
        SparseFloatMatrix2D Aco = new SparseFloatMatrix2D(matrix);
        A = Aco.getRowCompressed(true);
    }

    protected void tearDown() throws Exception {
        A = null;
        b = bt = x = null;
        xd = null;
        solver = null;
    }

    public void testSolve() {
        try {
            solver.solve(A, b, x);
            assertSolved();
        } catch (IterativeSolverFloatNotConvergedException e) {
            fail("Solver did not converge: " + e.getReason() + ". Residual=" + e.getResidual());
        }
    }

    public void testRepeatSolve() {
        try {
            for (int i = 0; i < repeat; ++i) {
                solver.solve(A, b, x);
                assertSolved();
                x = new DenseFloatMatrix1D(A.rows());
            }
        } catch (IterativeSolverFloatNotConvergedException e) {
            fail("Solver did not converge: " + e.getReason() + ". Residual=" + e.getResidual());
        }
    }

    protected void assertSolved() {
        float numerator = 0;
        float denominator = 0;
        float tmp;

        for (int i = 0; i < xd.size(); ++i) {
            tmp = xd.getQuick(i) - x.getQuick(i);
            numerator += (tmp * tmp);
            tmp = xd.getQuick(i);
            denominator += (tmp * tmp);
        }
        assertEquals(0.0, (float) Math.sqrt(numerator) / (float) Math.sqrt(denominator), rmse);
    }

